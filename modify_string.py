#!/usr/bin/env python3
string ="coding for beginners in easy steps"

print("\nCapitalized:\t",string.capitalize())
print("\nTitled:\t\t",string.title())
print("\nCentered:\t",string.center(40," "))
print("\nUpper case:\t",string.upper())
print("\nLower case:\t",string.lower())
print("\n Joined:\t",string.join("**"))
print("\njustified:\t",string.rjust(40,"*"))
print("\nReplaced:\t",string.replace("s","&"))

